

def debug(msg: str):
    """Prints a debug message."""
    print(f"[DEBUG] {msg}")


def info(msg: str):
    """Prints an info message."""
    print(f"[INFO] {msg}")


def warning(msg: str):
    """Prints a warning message."""
    print(f"[WARNING] {msg}")


def error(msg: str):
    """Prints an error message."""
    print(f"[ERROR] {msg}")
